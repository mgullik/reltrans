      function dISCO(a)
      !ISCO in Rg 
      implicit none
      double precision a,dISCO,z1,z2
      z1 = ( 1.0 - a**2.0 )**(1.0/3.0)
      z1 = z1 * ( (1.0+a)**(1.0/3.0)+(1.0-a)**(1.0/3.0))+1.0
      z2 = sqrt( 3.0 * a**2.0 + z1**2.0 )
      if(a.ge.0.0)then
        dISCO = 3.0 + z2 - sqrt( (3.0-z1) * (3.0 + z1 + 2.0*z2) )
      else
        dISCO = 3.0 + z2 + sqrt( (3.0-z1) * (3.0 + z1 + 2.0*z2) )
      end if
      return
      end
